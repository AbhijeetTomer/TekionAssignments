package com.example.GameOfCricketAssignment.CricketGame;

public class Bowling {
  public int no_of_wickets;
  public int runs_conceded;
  public int overs;

  public int getNo_of_wickets() {
    return no_of_wickets;
  }

  public void setNo_of_wickets(int no_of_wickets) {
    this.no_of_wickets = no_of_wickets;
  }

  public int getRuns_conceded() {
    return runs_conceded;
  }

  public void setRuns_conceded(int runs_conceded) {
    this.runs_conceded = runs_conceded;
  }

  public int getOvers() {
    return overs;
  }

  public void setOvers(int overs) {
    this.overs = overs;
  }
}
